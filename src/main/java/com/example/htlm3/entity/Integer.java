package main.java.com.example.htlm3.entity;

public class Integer {
    public int year;
    public int ren;

    public Integer() {
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getRen() {
        return ren;
    }

    public void setRen(int ren) {
        this.ren = ren;
    }

    public Integer(int year, int ren) {
        this.year = year;
        this.ren = ren;
    }
}
